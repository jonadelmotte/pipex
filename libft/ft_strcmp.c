#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	const unsigned char	*ss1;
	const unsigned char	*ss2;
	size_t				i;

	ss1 = (const unsigned char *)s1;
	ss2 = (const unsigned char *)s2;
	i = 0;
	while (s1[i] == ss2[i] && ss1[i])
		i++;
	return ((int)(ss1[i] - ss2[i]));
}
